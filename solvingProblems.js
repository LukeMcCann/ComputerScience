// What is an Algorithm?
//
// Algorithm has become a bit of a buzzword
// for the laymen the most common place to hear
// this word is on advertisements for any
// "smart" technology. 
//
// In reality, algorithm is just what we call
// a process or "set of steps" we undergo to
// accomplish a task. 
//
// An algorithm can be as complex as 
// AES 256 encryption, or as simple as
// a basic recipe in a cookbook. 
//
// We can take two steps to becoming better 
// problem solvers:
//          1. Devise a plan
//          2. Master common solving patterns